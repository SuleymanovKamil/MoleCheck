import SwiftUI

struct Colors{
    let primary = Color("Primary")
    let background = Color("Background")
    let black = Color("Black")
    let yellow = Color("Yellow")
    let pink = Color("Pink")
    let gray = Color("Gray")
}

extension Color {
    static let colors = Colors()
}
